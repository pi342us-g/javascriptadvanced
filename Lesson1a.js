// string - represent text inside quotes
let str = "Hello , Javascript"
console.log(str)
console.log(typeof str)

// Number - represent both interger and floating point numbers

let num =  42
console.log("the number is "+ num)
console.log(typeof num)

let num2 = 42.66
console.log("the number is "+ num2)
console.log(typeof num2)

// BigInt - used for very large intergers
