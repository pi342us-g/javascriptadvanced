// Boolean - represent true or false
let isAvailable = true;
let isDone = false;
console.log(typeof isAvailable);
console.log(isDone);

// undefined - a variable declared but value not assigned
let notAssigned;
console.log(typeof notAssigned)

// null - it represent intentional absence of any object . it evaluates as an object
let emptyValue = null;
